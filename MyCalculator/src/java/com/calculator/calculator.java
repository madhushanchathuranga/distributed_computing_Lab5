package com.calculator;

/**
 *
 * @author madhushan
 */
public class calculator {
    
    public int add(int a,int b)
    {
        return a+b;
    }
    
    public int subs(int a,int b)
    {
        return a-b;
    }
    
    public int multi(int a,int b)
    {
        return a*b;
    }
    
    public int divi(int a,int b)
    {
        return a/b;
    }
    
}
